%%-----------  参数根轨迹 -------------%%
% G(s)H(s) = 10(1+Ks)/s(s+2)
% => D(s) = s^2+(2+10K)s+10 = 0;
% => 1+10Ks/(s^2+2s+10) = 0
% => G'(s)=10Ks/(s^2+2s+10) = K's/(s^2+2s+10)  K'=10K
num=[1 0];
den=[1 2 10];
G=tf(num,den);
rlocus(G);
xlabel('Re');
ylabel('Im');

